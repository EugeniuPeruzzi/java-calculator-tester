package org.java;

public class Ticket {
	private int km;
	private int age;
	
	public Ticket(int km, int age) {
		setKm(km);
		setAge(age);
	}

	public int getKm() {
		return km;
	}

	public void setKm(int km) {
		this.km = km;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
	
	 
}
